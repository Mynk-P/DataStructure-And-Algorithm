#include <iostream>
using namespace std;
int main(){
    //  int/int = int
    int a = 2/10;
    cout << a << endl;
    int b = 7.9/2;
    cout << b << endl;
    // Double
    double c = 1.23/2;
    cout << c << endl;
    // Relational Operator
    int d = 150;
    int e = 160;
    cout << (d==e) << endl;
    cout << (d<=e) << endl;
    cout << (d>=e) << endl;
    cout << (d!=e) << endl;

}